# -*- coding: utf-8 -*-
import os, sys

# Assignment of variables

# Using pyton, you can assignment variables just putting a letter (or a character set) followed by equal sign (=)
a = 1
b = 2
c = 3
# Variables that receive strings must be the strings in quotes
x = "Guido"
y = "van Rossum"
z = "father"

# Sum
# Você pode somar apenas usando o sinal básico de soma (+)
# Para soma, é possível usar tanto variáveis, variáveis e números e apenas números
d = a + b + c
e = a + 2 + 3
f = 1 + 2 + 3

# Subtraction
# Você pode subtrair, assim como na soma, apenas usando o sinal básico (-)
# Para subtração, é possível usar tanto variáveis, variáveis e números e apenas números
g = b - a
h = b - 1
i = 2 - 1

# Multiplication
j = b * c
k = b * 3
l = 3 * 2

# Division
m = c / b
n = c / 2
o = 3 / 2 